// Types of Operators in Javascript
// 1- Unary Operators --> Operators with only one operand
// 2- Binary Operators --> Operators with two operands

var num1 = 23;
console.log(++num1);
console.log(num1++);
console.log(num1);

var num2 = 5;

console.log('num1 + num2 = ' + (num1 + num2));
console.log('num1 - num2 = ' + (num1 - num2));
console.log('num1 * num2 = ' + (num1 * num2));
console.log('num1 / num2 = ' + (num1 / num2));
console.log('num1 ** num2 = ' + (num1 ** num2));